          <h3 class="fontsize-36 text-headline col-lg-12">幼兒的感覺統合發展</h3>

          <aside class=" col-lg-6 text-left p-gap-30">
            
            <p>　　我們的身體與生俱來就有許多的感覺，如感官的視覺、聽覺、觸覺、運動覺、平衡覺等，時時刻刻都在將我們身體內外各個器官、部位的情況，以及與生活環境接觸的情形，經由神經系統傳達到腦幹部，再由猶如指揮總部的腦部將這些訊息加以組織、整理、判斷、也就是將許多部份的感覺統合成為一個整體，再給予該怎麼做的命令，也就是指揮身體和外在環境做適當的互動和反應，因而形成認知和學習。</p>

            <p>感覺統合是逐漸備受關注的一個議題，以往孩子出現學習或問題行為時，不明就裡的父母常會以為孩子是調皮、搗蛋、故意使壞，或是偷懶，不願學習；但隨著感覺統合理論的日漸推廣，大家越來越發現有些時候孩子是不能也，而非不為！嬰幼兒期的感覺統合發展不良，可能是兒童學習困難的主要原因之一，所以近年來，感覺教育的重要性已逐漸被肯定。</p>

            <p>從教育現場的觀察來看，現代家長基因的細微變化以孩子身處密集都市化的生長環境，在成長過程中，嚴重缺乏可以促進身體感覺統合發展的活動機會，例如：攀爬、旋轉、盪鞦韆等，導致有越來越多的孩子在感覺統合的發展上出現問題，這些狀況亦將持續影響孩子日後的學習、生活自理與人際關係，其嚴重性不容小覷！</p>


